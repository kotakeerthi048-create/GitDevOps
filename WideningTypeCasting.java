public class WideningTypeCasting
{
    public static void main(String[]args)
    {
        int a = 12;
        long b = 13;
        float c = 14;
        double d = 15;
        System.out.println("Befor conversion int:"+a);
        System.out.println("After conversion long:"+b);
        System.out.println("After conversion float:"+c);
        System.out.println("After convesion double:"+d);
    }
}