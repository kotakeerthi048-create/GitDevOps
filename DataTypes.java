class DataTypes
 {
   public static void main(String args[])
   {
     byte a = 127;
     short b = 12456;
     int c = 234567;
     long d = 7794877048L;
     float e = 12.56F;
     double f = 1234.56;
     System.out.println("byte value="+a);
     System.out.println("Short value="+b);
     System.out.println("Int value="+c);
     System.out.println("long valure="+d);
     System.out.println("float value="+e);
     System.out.print("double value="+f);
   }
 }
  